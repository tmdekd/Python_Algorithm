# -*- coding: utf-8 -*-
# 문제:
# 1~200 사이의 정수 가운데 7의 배수이면서 5의 배수는 아닌 모든 숫자들을 찾아
# 콤마(,)로 구분된 문자열을 구성해 출력하는 프로그램을 작성하십시오.
#
# [입력 예시]
#
# [출력 예시]
# 7,14,21,28,42,49,56,63,77,84,91,98,112,119,126,133,147,154,161,168,182,189,196

result = []
for i in range(1, 201):
    if i % 7 == 0 and i % 5 != 0:
        result.append(str(i))
        
# join()은 리스트(또는 다른 이터러블)의 요소들을 특정 구분자로 연결해 하나의 문자열을 생성하는 메서드입니다.
# 여기서 ','는 각 숫자 사이에 콤마를 넣기 위한 구분자로 사용되었습니다.
print(','.join(result))

print("==============================================================================")
output = ""
for num in range(1, 201):
    if num % 7 == 0 and num % 5 != 0:
        # 첫 번째 숫자가 아닌 경우 앞에 콤마 추가
        # Python에서 빈 문자열("")은 False로 평가되며, 그 외에 어떤 값이든(문자열이 들어 있는 경우) True로 평가됩니다.
        if output:
            output += ","
        output += str(num)  # 문자열에 숫자를 추가

# 최종적으로 출력
print(output)