# -*- coding: utf-8 -*-
# 문제:
# 100~300 사이의 숫자에서 각각의 자리 숫자가 짝수인 숫자를 찾아
# 콤마(,)로 구분해 출력하는 프로그램을 작성하십시오.
#
# [입력 예시]
# ex_071_input.txt
#
# [출력 예시]
# 200,202,204,206,208,220,222,224,226,228,240,242,244,246,248,260,262,264,266,268,280,282,284,286,288

# 결과를 저장할 빈 문자열
output = ""

# 100에서 300 사이의 숫자 중 각각의 자리 숫자가 모두 짝수인 숫자를 찾기
for num in range(100, 301):
    # 숫자를 문자열로 변환한 후 각 자리를 확인
    num_str = str(num)
    is_all_even = True  # 모든 자리가 짝수인지 확인하기 위한 플래그

    # 각 자리 숫자를 하나씩 확인
    for digit in num_str:
        if int(digit) % 2 != 0:  # 만약 홀수인 자리가 있으면
            is_all_even = False  # 플래그를 False로 설정
            break  # 더 이상 확인할 필요가 없으므로 루프 종료

    # 모든 자리가 짝수인 경우 결과 문자열에 추가
    # output 문자열을 만드는 과정에서 숫자들을 하나씩 추가할 때만 그 앞에 콤마를 넣고 있기 때문에 마지막 요소 이후에 콤마가 없다.
    if is_all_even:
        if output:  # 출력 문자열이 비어있지 않으면 콤마 추가
            output += ","
        output += num_str  # 짝수인 숫자를 출력 문자열에 추가

# 최종 결과 출력
print(output)
