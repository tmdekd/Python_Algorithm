# -*- coding: utf-8 -*-  # 인코딩 설정

# 피보나치 수열 10번째까지 출력하는 프로그램 (리스트 내포 사용)

# 피보나치 수열(Fibonacci sequence): 첫 번째와 두 번째 항이 1이고, 이후의 항은 이전 두 항의 합으로 정의되는 수열

# 초기 피보나치 수열 값 설정 (첫 번째와 두 번째 항은 1로 고정)
fib = [1, 1]  # 피보나치 수열의 첫 번째와 두 번째 값은 항상 1

# 리스트 내포를 사용하여 3번째 항부터 10번째 항까지 계산하여 fib 리스트에 추가
# 반복문에서 i는 2부터 9까지 (총 8번 반복)
[fib.append(fib[i-1] + fib[i-2]) for i in range(2, 10)]  # 3번째 항부터 (i=2) 시작해서 10번째 항까지

# fib[i-1]는 바로 이전 항 (i번째 항)
# fib[i-2]는 이전의 이전 항 (i-1번째 항)
# 예를 들어, i=2일 때 fib[1] + fib[0] = 1 + 1 = 2가 되어 3번째 항이 2로 설정됨
# 이 과정이 반복되어 4번째 항, 5번째 항...10번째 항까지 모두 계산됨

# 결과 출력
print(fib)  # 피보나치 수열의 10번째까지의 값을 출력
