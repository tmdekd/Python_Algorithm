# 한글 자모 범위를 정의한 튜플 리스트
dicBase = (('가','깋'),  # '가'부터 '깋'까지의 범위
           ('나','닣'),  # '나'부터 '닣'까지의 범위
           ('다','딯'),  # '다'부터 '딯'까지의 범위
           ('라','맇'),  # '라'부터 '맇'까지의 범위
           ('마','밓'),  # '마'부터 '밓'까지의 범위
           ('바','빟'),  # '바'부터 '빟'까지의 범위
           ('사','싷'),  # '사'부터 '싷'까지의 범위
           ('아','잏'),  # '아'부터 '잏'까지의 범위
           ('자','짛'),  # '자'부터 '짛'까지의 범위
           ('차','칳'),  # '차'부터 '칳'까지의 범위
           ('카','킿'),  # '카'부터 '킿'까지의 범위
           ('타','팋'),  # '타'부터 '팋'까지의 범위
           ('파','핗'),  # '파'부터 '핗'까지의 범위
           ('하','힣'))  # '하'부터 '힣'까지의 범위

# 주어진 단어 리스트 (inputWord)
inputWord = ['막', '부모님', '비용', '비행기', '원래', '처리', '최초', '꼴', '좀', '들다', '싶다',
             '수출', '계시다', '다', '뒤', '듣다', '함께', '아이', '무척', '보이다', '가지다', '그',
             '자르다', '데리다', '마리', '개', '정도', '옳다', '놀이', '뜨겁다']

# 결과를 저장할 리스트 내포 구문
# 1. 'dicBase'의 각 범위 ('start', 'end')를 반복함
# 2. 'inputWord' 리스트의 각 단어 'word'를 사전순으로 정렬
# 3. 해당 범위 ('start' <= word[0] <= 'end')에 포함되는 단어만 추출
# 4. 각 범위에 맞는 단어들을 사전순으로 정렬하여 리스트로 묶음
result = [[word for word in sorted(inputWord) if start <= word[0] <= end] for start, end in dicBase]

# 결과 출력: 각 범위에 따라 단어들이 분류되고 사전순으로 정렬된 2차원 리스트가 출력됨
print(result)
