"""
백준 2750번: 수 정렬하기

[문제 설명]
- N개의 정수가 주어졌을 때, 이를 오름차순으로 정렬하는 프로그램을 작성한다.
- 수는 중복되지 않는다.

[입력]
- 첫째 줄: 정수 N (1 ≤ N ≤ 1,000)
- 둘째 줄부터 N개의 줄: 정수 (절댓값 ≤ 1,000)

[출력]
- 오름차순으로 정렬한 결과를 한 줄에 하나씩 출력한다.

[예제 입력]
5
5
2
3
4
1

[예제 출력]
1
2
3
4
5
"""
n = int(input())
num = []

for i in range(n):
    num.append(int(input()))
    
num.sort()

for i in num:
    print(i)