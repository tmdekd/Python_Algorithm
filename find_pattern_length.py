# 문제 : 패턴에서 반복되는 부분을 마디라고 부른다. 문자열을 입력 받아 마디의 길이를 출력하는 프로그램을 작성하라.
# -*- coding: utf-8 -*-

# 첫 줄에서 테스트 케이스의 개수 T를 입력받음
T = int(input())

# 각 테스트 케이스에 대해 반복
for test_case in range(1, T + 1):
    # 각 테스트 케이스마다 문자열을 입력받음
    s = input()

    # 마디의 길이를 찾기 위해 1부터 9까지의 길이를 시도 (문제에서 마디의 최대 길이는 10이라고 제시됨)
    for j in range(1, 10):
        # 문자열의 앞 부분 (0 ~ j-1)과 그 다음 부분 (j ~ 2j-1)이 동일한지 확인
        # 즉, s[:j]가 s[j:2j]와 같다면, 마디 길이가 j라는 의미
        if s[:j] == s[j:2 * j]:
            # 조건을 만족하는 가장 짧은 마디 길이 j를 출력
            print(f'#{test_case} {j}')
            # 현재 테스트 케이스에서 마디를 찾았으므로, 내부 루프를 빠져나감
            break