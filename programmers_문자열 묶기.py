def solution(strArr):
    # 문제 조건: 문자열의 최대 길이는 30
    # 따라서 인덱스를 문자열 길이로 사용할 수 있는
    # 길이 31짜리 리스트를 준비 (a[0]은 사용하지 않음)
    a = [0] * 31

    # 주어진 문자열 배열을 순회하며
    for x in strArr:
        # 각 문자열의 길이를 구하고,
        # 그 길이에 해당하는 위치의 카운트를 +1
        a[len(x)] += 1

    # 가장 많은 문자열이 속한 그룹의 크기를 반환
    return max(a)


if __name__ == "__main__":
    # 예제 입력
    strArr = ["a","bc","d","efg","hi"]

    # 문자열들을 길이별로 그룹화하면:
    # 길이 1 → ["a","d"] (2개)
    # 길이 2 → ["bc","hi"] (2개)
    # 길이 3 → ["efg"] (1개)
    # 따라서 최대 그룹 크기는 2
    print(solution(strArr))  # 출력: 2
